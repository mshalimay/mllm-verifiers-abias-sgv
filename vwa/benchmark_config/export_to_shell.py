"""
Shell integration utilities for constants.py

This module provides functions to export Python constants to shell environment variables
"""

import subprocess
from typing import Dict


def export_env_vars() -> Dict[str, str]:
    """Export environment variables for shell scripts"""
    # Import here to avoid circular imports
    from benchmark_config.constants import (
        BASE_URL,
        CLASSIFIEDS_DOCKER_COMPOSE_DIR,
        CLASSIFIEDS_RESET_TOKEN,
        DOCKER_IMGS_PATH,
        HOMEPAGE_PATH_VWA,
        HOMEPAGE_PATH_WA,
        URLS_DIR,
        URLS_FILE_TEMPLATE,
    )

    env_vars = {
        "URLS_DIR": URLS_DIR,
        "URLS_FILE_TEMPLATE": URLS_FILE_TEMPLATE,
        "CLASSIFIEDS_DOCKER_COMPOSE_DIR": CLASSIFIEDS_DOCKER_COMPOSE_DIR,
        "DOCKER_IMGS_PATH": DOCKER_IMGS_PATH,
        "HOMEPAGE_PATH_WA": HOMEPAGE_PATH_WA,
        "HOMEPAGE_PATH_VWA": HOMEPAGE_PATH_VWA,
        "BASE_URL": BASE_URL,
        "CLASSIFIEDS_RESET_TOKEN": CLASSIFIEDS_RESET_TOKEN,
    }
    return env_vars


def generate_shell_env_file(output_path: str = "benchmark_config/env_constants_generated.sh") -> str:
    """Generate a shell environment file from Python constants"""
    env_vars = export_env_vars()

    with open(output_path, "w") as f:
        f.write("# Auto-generated environment constants from benchmark_config/constants.py\n")
        f.write("# Do not edit this file directly - edit benchmark_config/constants.py instead\n\n")

        for key, value in env_vars.items():
            f.write(f"{key}='{value}'\n")

    print(f"Generated shell environment file: {output_path}")
    return output_path


def load_shell_env(shell_script_path: str) -> Dict[str, str]:
    """Legacy function - kept for backward compatibility"""
    command = f"set -a && source {shell_script_path} && env"
    result = subprocess.run(["bash", "-c", command], capture_output=True, text=True, check=True)
    env = {}
    for line in result.stdout.splitlines():
        if "=" in line:
            key, value = line.split("=", 1)
            env[key] = value
    return env


if __name__ == "__main__":
    # Generate shell environment file
    generate_shell_env_file()

    # Print current values
    env_vars = export_env_vars()
    for key, value in env_vars.items():
        print(f"{key}={value}")
